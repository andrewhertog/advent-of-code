func isWin(round []string) int {
	you := round[0]
	me := round[1]
	res := 99
	switch me {
	case "X":
		if you == "A" { // rock ties rock
			res = 0
		} else if you == "B" { // rock loses to paper
			res = -1
		} else { // rock beats sissors
			res = 1
		}
	case "Y":
		if you == "A" { // paper beats rock
			res = 1
		} else if you == "B" { // paper ties paper
			res = 0
		} else { // paper loses to sissors
			res = -1
		}
	case "Z":
		if you == "A" { //sissors looses to rock
			res = -1
		} else if you == "B" { // sissors beats paper
			res = 1
		} else { // sissors ties sissors
			res = 0
		}
	}
	return res
}
